/**
 * Анализ эмоциональных маркеров
 */
function analyzeEmotions($text) {
    $emotions = [
        'anxiety' => ['тревог', 'боюсь', 'страх', 'волн', 'пережив', 'беспоко', 'нерв', 'пуга'],
        'interest' => ['интерес', 'любопыт', 'увлек', 'захватыв', 'интриг'],
        'confusion' => ['растерян', 'непонят', 'запут', 'не знаю', 'сложно', 'не понимаю'],
        'excitement' => ['азарт', 'восторг', 'энерг', 'вдохнов', 'рад'],
        'sadness' => ['груст', 'печал', 'тоск', 'расстроен', 'огорч'],
        'confidence' => ['уверен', 'знаю', 'понимаю', 'ясно', 'точно']
    ];
    
    $text = mb_strtolower($text);
    $detected = [];
    
    foreach ($emotions as $emotion => $markers) {
        foreach ($markers as $marker) {
            if (mb_strpos($text, $marker) !== false) {
                $detected[$emotion] = ($detected[$emotion] ?? 0) + 1;
            }
        }
    }
    
    arsort($detected);
    return array_keys($detected)[0] ?? 'default';
}

/**
 * Получить вопрос для текущего этапа
 */
function getQuestionForStage($scenario, $stage, $userInput, $usedQuestions = []) {
    if (!isset($scenario['stages'][$stage])) {
        return $scenario['greeting'];
    }
    
    $questions = $scenario['stages'][$stage];
    
    // Определяем подкатегорию по эмоциям (для модуля uncertainty)
    if ($stage === 'emotion' && isset($userInput)) {
        $emotion = analyzeEmotions($userInput);
        if (isset($questions[$emotion])) {
            $questions = $questions[$emotion];
        } elseif (isset($questions['default'])) {
            $questions = $questions['default'];
        }
    }
    
    // Если это массив вопросов
    if (is_array($questions) && !empty($questions)) {
        // Фильтруем уже использованные
        $available = array_diff($questions, $usedQuestions);
        if (empty($available)) {
            $available = $questions;
        }
        return $available[array_rand($available)];
    }
    
    return $questions;
}

/**
 * Определить следующий этап
 */
function getNextStage($scenario, $currentStage, $messageCount) {
    $stageOrder = array_keys($scenario['stages']);
    $currentIndex = array_search($currentStage, $stageOrder);
    
    if ($currentIndex === false) {
        return $stageOrder[0] ?? 'emotion';
    }
    
    // Переходим на следующий этап каждые 2 сообщения
    $nextIndex = $currentIndex + 1;
    
    if ($nextIndex >= count($stageOrder)) {
        return null; // Завершение
    }
    
    // Проверяем, достаточно ли сообщений на текущем этапе
    $messagesOnCurrentStage = $messageCount % 2;
    
    if ($messagesOnCurrentStage === 0 && $messageCount > 0) {
        return $stageOrder[$nextIndex];
    }
    
    return $currentStage;
}
